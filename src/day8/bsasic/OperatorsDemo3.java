package day8.bsasic;

public class OperatorsDemo3 {

	public static void main(String[] args) {
		// 3. Relational Operators > < >= <= == !=
		int a = 5;
		int b = 6;
		System.out.println(a > b);// false
		System.out.println(a < b);// true
		System.out.println(a >= b);// false
		System.out.println(a <= b);// true
		System.out.println(a == b);// false
		System.out.println(a != b);// true
	}

}
