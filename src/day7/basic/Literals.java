package day7.basic;

public class Literals {

	public static void main(String[] args) {
		// Literals: source code representation of fixed value.
		// Literals: fixed value + dataatype
		// Integer Literals: 125, 4435 45 5466363543
		byte b = 127;
		short s = 32767;
		;
		int i = 2147483647;
		long l1 = 2147483647;
		long l2 = 2147483648L;
		long l3 = 9223372036854775807L;

		// 2. Floating Literals: 45.67 23.78 (double)
		float f = 23.45F;
		double d = 23.45;

		// 3. Boolean Literals: true false
		boolean bb1 = true;
		boolean bb2 = false;

		// 4. Character Literals: 'a' '?' '1'
		char c1 = 'a';
		char c2 = '?';
		char c3 = '1';
		char c4 = '\u0000';

		// 5. String Literals: "a" "abc"
		String s1 = "abc@123#5a";
		String s2 = "a";
		String s3 = "123";
		String s4 = "34.56";
		String s5 = "true";
		String s6 = "Hello, My name is Jagat. I am from Nepal.";
		String s7 = "";
		String s8 = " ";

		// 6. Null Literals: null
		String s9 = null;
		//LiteralsDemo obj = new LiteralsDemo();// this is an object of literalsDemo class
		//literalsDemo ob = null;
	}

}
