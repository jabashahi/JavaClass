package day5.basic;

public class VariableDemo {

	public static void main(String[] args) {

		// Single variable declaration/definition
		int a;
		// Single variable initialization
		a = 5;

		// Single variable declaration/definition
		int b = 6;

		// multiple variable declaration/definition
		int x, y, z;
		// multiple variable initialization
		x = 3;
		y = 5;
		z = 8;

		// multiple variable declaration/definition
		int c = 6, d = 7, e = 9;
		
		int p, q, r;
		p = q = r = 9;
		
		int m = 7;
		int n = m;
		
		
	}

}
